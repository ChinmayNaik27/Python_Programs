#python program for inheritance

class Teacher:
    def setid(self,id):
        self.id=id
    def setname(self,name):
        self.name=name
    def setprof(self,prof):
        self.prof=prof
    def setqual(self,qual):
        self.qual=qual
    def getid(self):
        return self.id
    def getname(self):
        return self.name
    def getprof(self):
        return self.prof
    def getqual(self):
        return self.qual

