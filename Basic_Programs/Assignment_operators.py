#this is using assignment operators
d=5                       #store value 5 in d
e=20                       #store value 20 in e
d+=1                       #d=d+1 and store value in d
print(d)
e-=2                       #e=e-2 and store value in e
print(e)
d*=2                       #d=d*2 and store value in d
print(d)
e/=5                        #e=e/5 and store value in e , this is used to store float value
print(e)
d//=3                       #d=d//3 and store value in d , this is used to store integer value
print(d)
e%=3                       #e=e%3 and store value in e
print(e)