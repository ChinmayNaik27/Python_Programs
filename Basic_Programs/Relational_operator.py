# this is program for relational operators
a=9                      # Set value of a to 9
b=15                     #Set value of b to 15
print(a>b)              #if a greater than b ,displays true
print(a<b)               #if b greater than a,displays true
print(a==b)                  #if a equal to b ,displays true
print(a!=b)              #if a not equal to b ,displays true
print(a>=b)                   #if a greater than or equal to b ,displays true
print(a<=b)                      #if b greater than or equal to a ,displays true