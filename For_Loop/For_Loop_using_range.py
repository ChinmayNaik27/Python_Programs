for i in range(1,10,2):                           #for every value of i Starting from 1,stop at 10 with step=2
    print(i)
# 2nd:
for j in range(10):                               #for every value of i Starting from 1,stop at 10 with step=1;
    print(j)
# 3rd:
for k in range(10,0,-1):                          #for every value of i Starting from 10,stop at 0 with step=-1
    print(k)                                      #print loop in reverse
#4th:
for l in range(1,10):                             #for every value of i Starting from 1,stop at 10 with step=1
    print(l)