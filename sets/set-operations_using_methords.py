#operations in set using methord operators
set1={1,2,3,4,5,6,7}
set2={3,4,5,6,8,9}
#union
a=set1|set2
print("union of set is:",a)
print("=====================================")
#intersection
a=set1&set2
print("Intersection of set is:",a)
#difference
a=set1-set2
print("Difference:",a)
#symetric Diffrence
a=set1^set2
print("Symmetric Difference:",a)
